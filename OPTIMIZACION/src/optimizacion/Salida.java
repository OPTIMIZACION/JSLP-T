    public Salida(TableModel modelo, String titulo, String variables, String restricciones) {
        initComponents();
        this.setLocationRelativeTo(null);
        
        this.modelo=modelo;
        this.variables=variables;
        this.restricciones=restricciones;
        
        this.labelTitulo.setText(titulo);
        this.labelVariables.setText(variables);
        this.labelRestricciones.setText(restricciones);
        
        //bandera para decidir que metodos visualizar
        boolean bandera=true;
        if(Integer.parseInt(variables)==2)
        {
            
        
            for (int i = 0; i < modelo.getRowCount(); i++) 
            {
                if(">=".equals( modelo.getValueAt(i, modelo.getColumnCount()-2) )  )
                {
                    bandera=false;
                    break;
                }
            }
            if(bandera)
            {
                Grafico grafico=new Grafico(modelo, Integer.parseInt(variables), Integer.parseInt(restricciones) );
                JSPGrafico.setViewportView(grafico.getContentPane());
                JPGrafico.add(JSPGrafico);
            }
        }
        if(!bandera)
        {
            visualizarSimplexM();
        }
        if(bandera)
        {
            visualizarSimplex();
        }
    }

