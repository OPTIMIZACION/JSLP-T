    private void clickBotonVerDatosEntrada(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_clickBotonVerDatosEntrada
        
        int columnas = modelo.getColumnCount(); 
        int filas = modelo.getRowCount();
        String[][] matrizG = new String[filas][columnas];
        for(int i=0; i<filas; i++) { 
        for(int j=0; j<columnas; j++) 
            matrizG[i][j]=String.valueOf(modelo.getValueAt(i,j));  
        }
        System.out.println("TERMINO MATRIZ");
        
        VentanaEntradaDatos VE=new VentanaEntradaDatos(titulo,
            Integer.parseInt(variables),Integer.parseInt(restricciones),matrizG);
        VE.setVisible(true);
        this.setVisible(false); 
        
    }//GEN-LAST:event_clickBotonVerDatosEntrada

    public void visualizarSimplex()
    {
        System.out.println("Click en el tabpanel");

        SimplexM simplex=new SimplexM(modelo, Integer.parseInt(variables), Integer.parseInt(restricciones) );
        System.out.println("SALIO DEL CONSTRUCTOR");

        this.tablaSimplex = new JTable(simplex.getModelTabla()){
            public boolean isCellEditable(int fila, int columna)
            {
                return false;
            }
        };
        this.tablaSimplex.setAutoResizeMode(0);
        this.tablaSimplex.setCellSelectionEnabled(false);

        TableColumnModel columnModel = this.tablaSimplex.getColumnModel();
        for (int i = 0; i < columnModel.getColumnCount(); i++) {
            columnModel.getColumn(i).setPreferredWidth(100);
        }

        jScrollPaneSimplex.setViewportView(this.tablaSimplex);
        
        //////////////////////////////////////////////////////////////////////////////      
        
    }
    
